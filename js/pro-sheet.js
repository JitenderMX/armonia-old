// Event listener for category tab click
$('#categoryTabs li').click(function () {
  // Remove active class from all tabs
  $('#categoryTabs li').removeClass('active');
  $(".ct-text").text($(this).text());
  // Add active class to the clicked tab
  $(this).addClass('active');

  // Get the selected category from the data attribute
  const selectedPro = $(this).attr('data-subCategory');

  // Call the function to display City
  showProducts(selectedPro);
});

function showProducts(selectedCategory) {
  // alert(selectedCategory);
  const productsContainer = document.getElementById('productList');
  productsContainer.innerHTML = '';

  const productsInCategory = productSheet.filter(product => product['Sub-Category'] === selectedCategory);
  // const filteredProducts = productSheet.filter(product => product['Sub-Category'] === 'chairs');

  // console.log(filteredProducts);
  productsInCategory.forEach(product => {
    const productLink = document.createElement('a');
    productLink.href = `product.html#${product['Product-Code']}`;
    productLink.className = 'col-md-6 p-0';

    const productBox = document.createElement('div');
    productBox.className = 'product-box';

    const productImg = document.createElement('div');
    productImg.className = 'product-img';

    const img = document.createElement('img');
    img.src = `image/productImg/${product['Img-1']}`;
    img.className = 'w-100';
    img.alt = '';

    const productText = document.createElement('div');
    productText.className = 'product-text';

    const productName = document.createElement('div');
    productName.className = 'pro-name';
    productName.textContent = product.Name;

    const productDescription = document.createElement('div');
    productDescription.className = 'pro-des';
    productDescription.textContent = product.Description;

    // Append elements
    productImg.appendChild(img);
    productText.appendChild(productName);
    productText.appendChild(productDescription);
    productBox.appendChild(productImg);
    productBox.appendChild(productText);
    productLink.appendChild(productBox);
    productsContainer.appendChild(productLink);
    // console.log(productLink);
  });
  // if (productsInCategory <= 0) {


  // } else {
  //   productsContainer.appendChild('<p class="th-p">You selected wrong category</p>')
  // }

}
// Display initial City on page load
const initialCity = $('#categoryTabs li.active').attr('data-subCategory');
showProducts(initialCity);

